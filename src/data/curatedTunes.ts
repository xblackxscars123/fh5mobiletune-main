// Get featured tunes by discipline
export function getFeaturedByType(tuneType: TuneType): CuratedTune[] {
  return curatedTunes.filter(tune => tune.tuneType === tuneType);
}

// Get a random selection of featured tunes
export function getRandomFeatured(count: number = 4): CuratedTune[] {
  const shuffled = [...curatedTunes].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}

// Get featured tunes for the current week (rotates weekly)
export function getWeeklyFeatured(): CuratedTune[] {
  const now = new Date();
  const startOfYear = new Date(now.getFullYear(), 0, 1);
  const weekNumber = Math.floor((now.getTime() - startOfYear.getTime()) / (7 * 24 * 60 * 60 * 1000));
  
  // Rotate through different selections each week
  const tuneTypes: TuneType[] = ['grip', 'drift', 'rally', 'offroad', 'drag', 'street'];
  const featured: CuratedTune[] = [];
  
  tuneTypes.forEach((type, index) => {
    const typeTunes = getFeaturedByType(type);
    if (typeTunes.length > 0) {
      // Pick a different tune each week for each type
      const tuneIndex = (weekNumber + index) % typeTunes.length;
      featured.push(typeTunes[tuneIndex]);
    }
  });
  
  return featured;
}
